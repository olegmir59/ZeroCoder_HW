def sum_range(start, end):
    """
    Возвращает сумму всех целых чисел от start до end включительно, используя формулу Гаусса.
    """
    # Вычисляем количество чисел в диапазоне
    n = end - start + 1
    # Применяем формулу Гаусса
    total = (start + end) * n // 2
    return total

# Пример  10000 циклов  решает за 1 проход. Математикам и компьютерам такое больше нравится.


start = -8
end = 9999
result = sum_range(start, end)
print(f"Сумма чисел от {start} до {end}:", result)
