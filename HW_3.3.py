# Запрашиваем первую строку
first_string = input("Введите первую строку: ")

# Запрашиваем вторую строку
second_string = input("Введите вторую строку: ")

# Производим конкатенацию строк
concatenated_string = first_string + second_string

# Выводим объединённую строку
print("Объединённая строка:", concatenated_string)
